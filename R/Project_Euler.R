frac_2_dec <- function(d){
  decimal <- c(floor(1/d))
  r <- 1%%d
  remainder <- c()
  while (r%%d != 0 & !r %in% remainder){ 
    remainder <- c(remainder, r)
    r <- 10*r%%d
    decimal <- c(decimal, floor(r/d))
  }
  return(decimal)
}

